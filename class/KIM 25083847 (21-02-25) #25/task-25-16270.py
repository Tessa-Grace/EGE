from itertools import product

ans = []
for v in product('0123456789', repeat=1):
    v = ''.join(v)
    num = int(f'12{v}345{v}67089{v}')
    if num <= 10 ** 13 and num % 206 == 0:
        ans.append([num, num // 206])

ans = sorted(ans)
for i in ans:
    print(*i)

