from functools import lru_cache # чтобы закэшировать информацию, программа работает быстрее

@lru_cache(None) # None, чтобы не выделялась память
def f(n):
    if n < 11:
        return n
    if n >= 11:
        return n + f(n - 1)

for i in range(10, 2025):
    f(i)

print(f(2024) - f(2021))


