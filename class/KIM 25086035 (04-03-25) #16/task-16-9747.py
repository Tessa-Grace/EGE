from sys import setrecursionlimit
setrecursionlimit(10000) #тк в питоне есть ограничение на выполнение шагов рекурсии в 1000
                         # но память забивается, что неприятно
def f(n):
    if n < 11:
        return n
    if n >= 11:
        return n + f(n - 1)

print(f(2024) - f(2021))

# --------------------------------
